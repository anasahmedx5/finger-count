import pyfirmata2

comport = 'COM3'
board = pyfirmata2.Arduino(comport)

segment_a = board.get_pin('d:2:o')
segment_b = board.get_pin('d:3:o')
segment_c = board.get_pin('d:4:o')
segment_d = board.get_pin('d:5:o')
segment_e = board.get_pin('d:6:o')
segment_f = board.get_pin('d:7:o')
