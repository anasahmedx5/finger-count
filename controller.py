led_4=board.get_pin('d:12:o')
led_5=board.get_pin('d:13:o')

def clear_display():
    """Turn off all segments of the 7-segment display."""
    segment_a.write(0)
    segment_b.write(0)
    segment_c.write(0)
    segment_d.write(0)
    segment_e.write(0)
    segment_f.write(0)
    segment_g.write(0)
